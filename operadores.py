### Operadores ###

print("Operadores")
print(3 + 4) # Soma
print(3 - 4) # Subtraçã
print(3 * 4) # Multiplicação
print(4 / 2) # Divisão
print(10 % 2) # Módulo
print (10 // 3) # Pega apenas o valor inteiro da divisão
print(2 ** 3) # Potência

print("Hola " + "Python") # Também serve para concatenar
#Só se pode usar este tipo de concatenação entre duas Strings
#Se fizer print("Hola" + 5) dará erro
#Para funcionar tem que colocar print("Hola" + str(5))

print("Hola " * 5) #Irá aparecer a String o número de vezes que foi colocado

### Operadores Comparativos ###

print()
print("Operadores comparativos")
print(3 > 4) # Compara se é maior
print(3 < 4) # Compara se é menor
print(3 >= 4) # Verifica se é maior ou igual
print(3 <= 4) # Verifica se é menor ou igual
print(3 == 4) # Verifica se é igual
print(3 != 4) # Verifica se é diferente

### Operadores Lógicos ###
print()
